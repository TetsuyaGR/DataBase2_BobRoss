    public PaymentFrame(BobTable table, DatabaseController db) {
        initComponents();
        DefaultListModel itemList = (DefaultListModel) jList1.getModel();
        itemList.removeAllElements();
        Map<String, Integer> paraggelies = db.getAllParaggelies(table.getReceiptId());
        for(Map.Entry paraggelia : paraggelies.entrySet()) {
            itemList.addElement(new BobItem((String) paraggelia.getKey(), (int) paraggelia.getValue(), table.getReceiptId()));
        }
        jTextField1.setText(String.valueOf(db.getLogariasmo(table)) + "€");
        jTextField2.setText(db.getServitorosName(table.getReceiptId()));
    }

